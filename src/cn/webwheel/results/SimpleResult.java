package cn.webwheel.results;

import cn.webwheel.WebContext;

import javax.servlet.ServletException;
import java.io.IOException;

/**
 * abstract simple result
 * @see SimpleResultInterpreter
 */
abstract public class SimpleResult {

    protected WebContext ctx;

    protected String charset = "utf-8";
    protected String contentType;

    public abstract void render() throws IOException, ServletException;

    public String getCharset() {
        return charset;
    }

    public void setCharset(String charset) {
        this.charset = charset;
    }

    public String getContentType() {
        return contentType;
    }

    public void setContentType(String contentType) {
        this.contentType = contentType;
    }
}
